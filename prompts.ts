export function getPromptForDate(date: Date, customPrompts: string[] = []): string {
  const allPrompts = [...PROMPTS, ...customPrompts];
  if (!allPrompts.length) return 'كيف كان يومك؟';
  const start = new Date(date.getFullYear(), 0, 1);
  const diff = Math.floor((date.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
  const index = Math.abs(diff) % allPrompts.length;
  return allPrompts[index];
}

export function getPromptForToday(customPrompts: string[] = []): string {
  return getPromptForDate(new Date(), customPrompts);
}
